
public class Cliente {
    private int id;
    private String nombre, apellido, correo, telefono, direccion;

    public Cliente(int id, String nombre, String apellido, String correo, String telefono, String direccion) {
        this.id = id;
        this.nombre = nombre;
        this.apellido = apellido;
        this.correo = correo;
        this.telefono = telefono;
        this.direccion = direccion;
    }


}
